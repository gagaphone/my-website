/* =========================
   RENDER
========================= */

function render() {
  const img = getCurrentImage();
  if (!img || !mainImage) return;

  mainImage.style.opacity = 0.3;

  const preload = new Image();
  preload.src = img.src;

  preload.onload = () => {
    mainImage.src = img.src;
    mainImage.style.opacity = 1;
  };

  if (counter) {
    counter.textContent = `${state.index + 1} / ${state.filtered.length}`;
  }

  updateProgress();
}

/* =========================
   FILTERS
========================= */

function getUniqueCategories(data) {
  return ["all", ...new Set(data.map(i => i.category))];
}

function createFilters() {
  if (!filterBar) return;

  filterBar.innerHTML = "";

  getUniqueCategories(images).forEach(cat => {
    const btn = document.createElement("button");

    btn.className = "filter";
    btn.dataset.filter = cat;
    btn.textContent = cat;

    btn.addEventListener("click", () => setFilter(cat));

    filterBar.appendChild(btn);
  });

  filters = filterBar.querySelectorAll(".filter");
}

function setFilter(filter) {
  state.filter = filter;

  state.filtered =
    filter === "all"
      ? images
      : images.filter(i => i.category === filter);

  state.index = 0;

  render();
  updateFilterUI();
  softTransition();
}

function updateFilterUI() {
  filters.forEach(btn => {
    btn.classList.toggle(
      "active",
      btn.dataset.filter === state.filter
    );
  });
}

/* =========================
   NAVIGATION
========================= */

function next() {
  if (state.index < state.filtered.length - 1) {
    state.index++;
    render();
  }
}

function prev() {
  if (state.index > 0) {
    state.index--;
    render();
  }
}

/* =========================
   DESKTOP WHEEL NAVIGATION
========================= */

window.addEventListener(
  "wheel",
  (e) => {
    if (state.lock || !state.filtered.length) return;

    state.lock = true;

    e.deltaY > 0 ? next() : prev();

    setTimeout(() => {
      state.lock = false;
    }, 500);
  },
  { passive: true }
);

/* =========================
   MOBILE SWIPE SUPPORT
========================= */

let touchStartY = 0;
let touchEndY = 0;

window.addEventListener(
  "touchstart",
  (e) => {
    touchStartY = e.changedTouches[0].screenY;
  },
  { passive: true }
);

window.addEventListener(
  "touchend",
  (e) => {
    if (state.lock || !state.filtered.length) return;

    touchEndY = e.changedTouches[0].screenY;

    const swipeDistance = touchStartY - touchEndY;

    // ignore tiny swipes
    if (Math.abs(swipeDistance) < 50) return;

    state.lock = true;

    if (swipeDistance > 0) {
      // swipe up → next image
      next();
    } else {
      // swipe down → previous image
      prev();
    }

    setTimeout(() => {
      state.lock = false;
    }, 500);
  },
  { passive: true }
);

/* =========================
   PROGRESS
========================= */

function updateProgress() {
  if (!progressFill) return;

  const percent =
    ((state.index + 1) / state.filtered.length) * 100;

  progressFill.style.height = percent + "%";
}

